#include "HumanA.hpp"

/* ************************************************************************** */
/*                         Constructor & Destructor                           */
/* ************************************************************************** */

HumanA::HumanA(std::string name, Weapon &weapon) : _name(name),
													_weapon(weapon)
{
	std::cout << this->_name << " enters the battle." << std::endl;
	return ;
}

HumanA::~HumanA(void)
{
	std::cout << this->_name << " leaves the battle." << std::endl;
}
/* ************************************************************************** */
/*                             Private functions                              */
/* ************************************************************************** */


/* ************************************************************************** */
/*                             Public functions                               */
/* ************************************************************************** */

void	HumanA::attack(void) const
{
	std::cout << this->_name << " attacks with their " << this->_weapon.getType() << std::endl;
}
